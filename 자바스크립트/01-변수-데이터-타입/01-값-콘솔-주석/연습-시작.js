// --------------------------------------------------------------------------
// 연습: 숫자 값 콘솔 패널에 출력
// --------------------------------------------------------------------------
101 /* 숫자 값 */
console.log(101) /* 터미널 출력 */

// --------------------------------------------------------------------------
// 연습: 문자 값 콘솔 패널에 출력
// --------------------------------------------------------------------------
'문자 값 출력 연습' /* 문자 값 */
console.log("유정")

// --------------------------------------------------------------------------
// 연습: 논리 값 콘솔 패널에 출력
// --------------------------------------------------------------------------
true
false
console.log(true)
console.log(false)

// --------------------------------------------------------------------------
// 연습: 여러 값 콘솔 패널에 출력
// --------------------------------------------------------------------------
console.log(101,'유정',false)

// --------------------------------------------------------------------------
// 연습: 콘솔 패널에 오류(Error) 또는 경고(Warning, Warn) 출력 , 정보(info) - error는 색상 값으로 표현, warn은 색상 차이가 없어서 이모티콘 같은 걸로 구분줘야함
// --------------------------------------------------------------------------
console.error('❌ 문자 값은 따옴표로 묶어 출력하지 않으면 오류입니다.')
console.warn('⚠️ JavaScript의 데이터를 기억하려면 변수 또는 상수가 필요합니다.')
console.info('ℹ️ 터미널 콘솔의 기능을 다양하게 활용할 수 있습니다.')

// --------------------------------------------------------------------------
// 연습: 콘솔 그룹화 출력
// --------------------------------------------------------------------------
// 숫자 (정수)
console.group('숫자(정수)')
console.log(10)
console.log(101)
console.log(908)
console.groupEnd()

// 숫자 (소수)
console.group('숫자(소수)')
console.log(0.6)
console.log(0.23)
console.log(0.964)
console.groupEnd()
